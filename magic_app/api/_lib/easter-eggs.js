function randomReply(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function getEasterEggReply(text) {
  const t = text.toLowerCase();

  if ((t.includes('спасибо') || t.includes('благодарю'))
      && (t.includes('александр') || t.includes('создател') || t.includes('разработчик'))) {
    return { reply: randomReply(THANKS_REPLIES), notifyCreator: true };
  }

  if (t.includes('кто тебя создал') || t.includes('кто тебя сделал')
      || t.includes('кто твой создатель') || t.includes('откуда ты')
      || t.includes('кто тебя придумал') || t.includes('кто тебя написал')) {
    const all = [...CARLSON_STYLE, ...STARK_STYLE, ...MIXED_STYLE];
    return { reply: randomReply(all), notifyCreator: false };
  }

  return null;
}
