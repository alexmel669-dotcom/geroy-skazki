/**
 * Согласование русского текста по полу ребёнка (сервер).
 */

export function normalizeGender(value) {
  if (value === 'male' || value === 'm') return 'male';
  if (value === 'female' || value === 'f') return 'female';
  return 'unknown';
}

export function buildGenderPrompt(gender, childName) {
  const g = normalizeGender(gender);
  const name = childName ? ` (${childName})` : '';
  if (g === 'female') {
    return `Пол ребёнка${name}: ДЕВОЧКА. Обращайся только в ЖЕНСКОМ роде: «ты рада», «ты готова», «ты смелая». В третьем лице — «она». Не используй «он», «рад», «готов», «смелый» по отношению к ребёнку.`;
  }
  if (g === 'male') {
    return `Пол ребёнка${name}: МАЛЬЧИК. Обращайся только в МУЖСКОМ роде: «ты рад», «ты готов», «ты смелый». В третьем лице — «он». Не используй «она», «рада», «готова», «смелая» по отношению к ребёнку.`;
  }
  return `Пол ребёнка${name} не указан. Используй нейтральные формулировки без «рад/рада», «готов/готова».`;
}

